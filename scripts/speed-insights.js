/**
 * Vercel Speed Insights Integration
 * This script initializes Vercel Speed Insights for the DebateCraft website
 */

// Initialize the Speed Insights queue
(function() {
  // Create the queue function if it doesn't exist
  window.si = window.si || function() {
    (window.siq = window.siq || []).push(arguments);
  };
  
  // Initialize Speed Insights by injecting the script
  // This script will be automatically loaded by Vercel when deployed
  // For local development, this won't track any data
  if (typeof window !== 'undefined') {
    const script = document.createElement('script');
    script.defer = true;
    
    // Vercel will automatically replace this path when deployed
    // The _vercel/insights/script.js path is generated by Vercel
    script.src = '/_vercel/insights/script.js';
    
    script.onerror = function() {
      // Silently fail if Speed Insights is not available
      // This is expected in local development
      console.debug('Speed Insights script not loaded (expected in development)');
    };
    
    document.head.appendChild(script);
  }
})();
